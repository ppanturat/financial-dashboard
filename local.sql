-- ============================================================================
-- 1. CLEANUP EXISTING TABLES & OBJECTS
-- ============================================================================
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
DROP FUNCTION IF EXISTS public.handle_new_user();

DROP TABLE IF EXISTS public.follows CASCADE;
DROP TABLE IF EXISTS public.follow_requests CASCADE;
DROP TABLE IF EXISTS public.portfolio_holdings CASCADE;
DROP TABLE IF EXISTS public.portfolio_folders CASCADE;
DROP TABLE IF EXISTS public.portfolio_items CASCADE;
DROP TABLE IF EXISTS public.folders CASCADE;
DROP TABLE IF EXISTS public.global_metrics CASCADE;
DROP TABLE IF EXISTS public.profiles CASCADE;

-- ============================================================================
-- 2. TABLE DEFINITIONS
-- ============================================================================

-- Profiles Table (Houses core user identifier metadata)
CREATE TABLE public.profiles (
    id uuid PRIMARY KEY,
    username text NULL,
    name text NULL,
    display_name text NULL, -- Adjusted to NULL to prevent frontend insertion mismatch failures
    avatar_url text NULL,
    portfolio_public bool NOT NULL DEFAULT false,
    created_at timestamptz NULL DEFAULT NOW()
);

-- Global Metrics Table (Caches market calculations and AI scan data strings)
CREATE TABLE public.global_metrics (
    ticker varchar PRIMARY KEY,
    revenue_yoy numeric NULL,
    fcf numeric NULL,
    war_chest_ratio numeric NULL,
    last_updated timestamptz NULL,
    ai_scan jsonb NULL
);

-- Folders Table (User Watchlists)
CREATE TABLE public.folders (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    name varchar NOT NULL,
    created_at timestamptz NULL DEFAULT NOW()
);

-- Portfolio Items Table (Individual tickers within watchlists)
CREATE TABLE public.portfolio_items (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    folder_id uuid NOT NULL REFERENCES public.folders(id) ON DELETE CASCADE,
    ticker varchar NOT NULL,
    added_at timestamptz NULL DEFAULT NOW()
);

-- Portfolio Folders Table (Grouping mechanism for portfolio investment assets)
CREATE TABLE public.portfolio_folders (
    id int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    name text NOT NULL,
    is_public bool NULL DEFAULT false,
    created_at timestamptz NULL DEFAULT NOW()
);

-- Portfolio Holdings Table (Individual unit transactions per portfolio grouping)
CREATE TABLE public.portfolio_holdings (
    id int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    folder_id int8 NULL REFERENCES public.portfolio_folders(id) ON DELETE CASCADE,
    ticker text NOT NULL,
    amount numeric NOT NULL,
    buy_price numeric NOT NULL,
    created_at timestamptz NULL DEFAULT NOW()
);

-- Follow Requests Table (Pending user relationship connections)
CREATE TABLE public.follow_requests (
    id int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requester_user_id uuid NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    target_user_id uuid NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    status text NULL DEFAULT 'pending',
    created_at timestamptz NULL DEFAULT NOW()
);

-- Follows Table (Established active user follow associations)
CREATE TABLE public.follows (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    follower_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    followee_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    status text NOT NULL,
    created_at timestamptz NOT NULL DEFAULT NOW()
);

-- ============================================================================
-- 3. AUTOMATED AUTH SYNC TRIGGER
-- ============================================================================
-- Guarantees a public profile row is created even if frontend background sync drops out
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.profiles (id, username, name, display_name, avatar_url, portfolio_public)
  VALUES (
    new.id,
    LOWER(TRIM(COALESCE(new.raw_user_meta_data->>'username', ''))),
    COALESCE(new.raw_user_meta_data->>'name', ''),
    COALESCE(new.raw_user_meta_data->>'name', ''),
    COALESCE(new.raw_user_meta_data->>'avatar_url', NULL),
    false
  )
  ON CONFLICT (id) DO NOTHING;
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

