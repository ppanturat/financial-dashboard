-- Profiles Policies
CREATE POLICY "Profiles are readable by authenticated users" 
    ON public.profiles FOR SELECT TO authenticated USING (true);

CREATE POLICY "Users can manage their own profile" 
    ON public.profiles FOR ALL TO authenticated USING (auth.uid() = id);

-- Global Metrics Policies
CREATE POLICY "Global metrics are readable by authenticated users" 
    ON public.global_metrics FOR SELECT TO authenticated USING (true);

CREATE POLICY "Users can initialize missing tickers into metrics" 
    ON public.global_metrics FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "Users can modify metrics data records" 
    ON public.global_metrics FOR UPDATE TO authenticated USING (true);

-- Watchlist Folders Policies
CREATE POLICY "Users have full control over own watchlist folders" 
    ON public.folders FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- Watchlist Ticker Items Policies
CREATE POLICY "Users have full control over own watchlist assets" 
    ON public.portfolio_items FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- Portfolio Folders Policies
CREATE POLICY "Users have full control over own investment folders" 
    ON public.portfolio_folders FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Followers and public can review designated folders" 
    ON public.portfolio_folders FOR SELECT TO authenticated 
    USING (
        is_public = true 
        AND EXISTS (
            SELECT 1 FROM public.follow_requests
            WHERE requester_user_id = auth.uid()
              AND target_user_id = portfolio_folders.user_id
              AND status = 'accepted'
        )
        OR auth.uid() = user_id
    );

-- Portfolio Holdings Policies
CREATE POLICY "Users have full control over own item holdings" 
    ON public.portfolio_holdings FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Followers can view holdings inside shared folders" 
    ON public.portfolio_holdings FOR SELECT TO authenticated 
    USING (
        auth.uid() = user_id
        OR EXISTS (
            SELECT 1 FROM public.portfolio_folders pf
            WHERE pf.id = portfolio_holdings.folder_id
              AND pf.is_public = true
              AND EXISTS (
                  SELECT 1 FROM public.follow_requests fr
                  WHERE fr.requester_user_id = auth.uid()
                    AND fr.target_user_id = pf.user_id
                    AND fr.status = 'accepted'
              )
        )
    );

-- Follow Requests Policies
CREATE POLICY "Involved parties can manage follow requests" 
    ON public.follow_requests FOR ALL TO authenticated 
    USING (auth.uid() = requester_user_id OR auth.uid() = target_user_id)
    WITH CHECK (auth.uid() = requester_user_id OR auth.uid() = target_user_id);

-- Fallback Follows Connection Policies
CREATE POLICY "Involved accounts can interact with follows table" 
    ON public.follows FOR ALL TO authenticated 
    USING (auth.uid() = follower_id OR auth.uid() = followee_id)
    WITH CHECK (auth.uid() = follower_id OR auth.uid() = followee_id);

