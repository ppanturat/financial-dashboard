-- ============================================================================
-- 5. FINE-GRAINED SECURITY POLICIES
-- ============================================================================

-- --- PROFILES POLICIES ---
CREATE POLICY "Allow public read access to profiles" 
    ON public.profiles FOR SELECT TO authenticated USING (true);

CREATE POLICY "Allow users to update own profile" 
    ON public.profiles FOR UPDATE TO authenticated USING (auth.uid() = id);

CREATE POLICY "Allow system profile insertion" 
    ON public.profiles FOR INSERT TO authenticated WITH CHECK (auth.uid() = id);

-- --- GLOBAL METRICS POLICIES ---
CREATE POLICY "Allow authenticated read to global metrics" 
    ON public.global_metrics FOR SELECT TO authenticated USING (true);

CREATE POLICY "Allow write modification access to data handlers" 
    ON public.global_metrics FOR ALL TO authenticated USING (true) WITH CHECK (true);

-- --- WATCHLIST FOLDERS & ITEMS POLICIES ---
CREATE POLICY "Manage individual watchlist folders" 
    ON public.folders FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Manage individual watchlist items" 
    ON public.portfolio_items FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- --- PORTFOLIO FOLDERS POLICIES ---
CREATE POLICY "Allow owners full control over portfolio folders" 
    ON public.portfolio_folders FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Allow viewing of public portfolio folders" 
    ON public.portfolio_folders FOR SELECT TO authenticated 
    USING (
        is_public = true 
        OR auth.uid() = user_id 
        OR EXISTS (
            SELECT 1 FROM public.follows 
            WHERE follower_id = auth.uid() 
              AND followee_id = portfolio_folders.user_id 
              AND status = 'accepted'
        )
    );

-- --- PORTFOLIO HOLDINGS POLICIES ---
CREATE POLICY "Allow owners full control over holdings" 
    ON public.portfolio_holdings FOR ALL TO authenticated 
    USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Allow viewing holdings inside accessible folders" 
    ON public.portfolio_holdings FOR SELECT TO authenticated 
    USING (
        auth.uid() = user_id 
        OR EXISTS (
            SELECT 1 FROM public.portfolio_folders f
            WHERE f.id = portfolio_holdings.folder_id 
              AND (
                f.is_public = true 
                OR EXISTS (
                    SELECT 1 FROM public.follows fol 
                    WHERE fol.follower_id = auth.uid() 
                      AND fol.followee_id = f.user_id 
                      AND fol.status = 'accepted'
                )
              )
        )
    );

-- --- FOLLOW REQUESTS & RELATIONS POLICIES ---
CREATE POLICY "Manage involved follow requests" 
    ON public.follow_requests FOR ALL TO authenticated 
    USING (auth.uid() = requester_user_id OR auth.uid() = target_user_id)
    WITH CHECK (auth.uid() = requester_user_id OR auth.uid() = target_user_id);

CREATE POLICY "Manage visible follow relationships" 
    ON public.follows FOR ALL TO authenticated 
    USING (auth.uid() = follower_id OR auth.uid() = followee_id)
    WITH CHECK (auth.uid() = follower_id OR auth.uid() = followee_id);

